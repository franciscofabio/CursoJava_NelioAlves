package processamento_de_dados_casting;

public class Main {

	public static void main(String[] args) {
		// Exemplo 1
		int x, y;
		x = 5;
		y = 2 * x;
		System.out.println(x);
		System.out.println(y);
		
		// Exemplo 2
		int x_;
		double y_;
		x_ = 5;
		y_ = 2 * x;
		System.out.println(x_);
		System.out.println(y_);

	}

}
